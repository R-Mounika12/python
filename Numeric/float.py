# float variables

a = 4.2
b= 2.2

sum = a + b
print("sum ", sum)

diff = a-b
print("diff ", diff)

mul = a*b
print("mul ", mul)

div = a/b
print("div ", div)

mod = a%b
print("mod (reminder)", mod)

# Rounding
