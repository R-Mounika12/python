## Basic Assignment
num = 3 

##Addition Assignment (+=)
num+= 2
print("add : ", num)
##Subtraction Assignment (-=)
num-= 2
print("sub : ", num)
##Multiplication Assignment (*=)
num*= 2
print("mul : ", num)
##Division Assignment (/=)
num/= 2
print("div : ", num)
##Modulus Assignment (%=)
num%= 2
print("mod : ", num)
##floorDivision Assignment (//=)
num//= 2
print("floorDiv : ", num)


