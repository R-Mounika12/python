# if the block of code to be executed in definate number of times
# range(10) returns sequence of numbers from 0 to 9

for i in range(10):
    print("abc")

for i in [0, 9] :
    print(i)  # prints 0 and 9

numList = [1, 2,3,4,5]
for num in numList:
    print(num)

colors = ["red", "green", "blue", "yellow"]
for color in colors :
    print(color)
